"""
Demonstrates common Python literals.
"""


def show_literals() -> None:
    integer_literal = 42
    float_literal = 3.14
    string_literal = "hello"
    boolean_literal = True
    none_literal = None
    list_literal = [1, 2, 3]
    tuple_literal = ("a", "b")
    dict_literal = {"name": "Ada", "language": "Python"}
    set_literal = {1, 2, 3}

    print(integer_literal, float_literal, string_literal)
    print(boolean_literal, none_literal)
    print(list_literal, tuple_literal, dict_literal, set_literal)



show_literals()
