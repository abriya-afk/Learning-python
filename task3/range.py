"""
Demonstrates the range data type in Python.
"""

# Creating a range
my_range = range(5)
print(f"Original range: {list(my_range)}")

# Creating a range with start and stop
my_range = range(2, 8)
print(f"Range with start and stop: {list(my_range)}")

# Creating a range with start, stop, and step
my_range = range(2, 10, 2)
print(f"Range with step: {list(my_range)}")